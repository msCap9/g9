// Получить координаты города
export const getCityCoordinates = (cityName) => {
    // Нормализуем название города
    const normalizedCity = cityName.trim();

    // Точное совпадение
    if (russianCities[normalizedCity]) {
        return russianCities[normalizedCity];
    }

    // Поиск без учёта регистра
    const cityLower = normalizedCity.toLowerCase();
    for (const [city, coords] of Object.entries(russianCities)) {
        if (city.toLowerCase() === cityLower) {
            return coords;
        }
    }

    // Попытка найти частичное совпадение
    for (const [city, coords] of Object.entries(russianCities)) {
        if (city.toLowerCase().includes(cityLower) || cityLower.includes(city.toLowerCase())) {
            return coords;
        }
    }

    // По умолчанию - Москва
    return russianCities['Москва'];
};

// Получить список городов для выбора
export const getCityList = () => {
    return Object.keys(russianCities).sort();
};

